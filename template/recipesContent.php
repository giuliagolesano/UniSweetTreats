        <h2>Oreo Cake Recipe</h2>
        <img src="<?php echo CAKES_DIR . 'oreoCake.png'; ?>" alt="Oreo Cake">
        <div class="decorative-line"></div>
        <h3>Ingredients</h3>
        <h4>For the Base:</h4>
        <ul>
            <li>250 g (9 oz) Oreo cookies (without the cream filling)</li>
            <li>100 g (1/2 cup) melted butter</li>
        </ul>
        <h4>For the Cream Filling:</h4>
        <ul>
            <li>250 g (1 cup) mascarpone cheese</li>
            <li>250 g (1 cup) heavy whipping cream, cold</li>
            <li>100 g (3/4 cup) powdered sugar</li>
            <li>100 g (1/3 cup) white chocolate spread (optional)</li>
            <li>Oreo cream filling (collected from the cookies)</li>
        </ul>
        <h4>For Decoration:</h4>
        <ul>
            <li>8–10 whole Oreo cookies</li>
            <li>50 g (1.7 oz) melted dark chocolate (optional)</li>
        </ul>
        <h3>Instructions</h3>
        <ol>
            <li>Prepare the base by crushing Oreo cookies and mixing with melted butter.</li>
            <li>Press the mixture into a springform pan and chill.</li>
            <li>Make the cream filling by whipping cream, mixing mascarpone with powdered sugar, and folding everything together.</li>
            <li>Spread the cream filling over the base and refrigerate for at least 4 hours.</li>
            <li>Decorate with whole Oreos and melted chocolate before serving.</li>
        </ol>
