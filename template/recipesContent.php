    <div class="recipe">
        <h2>Berry Muffins Recipe</h2>
        <img src="<?php echo CUPCAKES_DIR . 'berriesCupcake.png'; ?>" alt="Berry Muffins">
        <div class="decorative-line"></div>
        <h3>Ingredients</h3>
        <h4>For the Muffins:</h4>
        <ul>
            <li>250 g (2 cups) all-purpose flour</li>
            <li>150 g (3/4 cup) granulated sugar</li>
            <li>2 tsp baking powder</li>
            <li>1/2 tsp salt</li>
            <li>100 g (1/2 cup) unsalted butter, melted</li>
            <li>2 large eggs</li>
            <li>120 ml (1/2 cup) milk</li>
            <li>1 tsp vanilla extract</li>
            <li>150 g (1 cup) mixed berries (blueberries, raspberries, etc.)</li>
        </ul>
        <h4>For Decoration:</h4>
        <ul>
            <li>Powdered sugar for dusting</li>
            <li>Additional berries (optional)</li>
        </ul>
        <h3>Instructions</h3>
        <ol>
            <li>Preheat your oven to 180°C (350°F) and line a muffin tin with paper liners.</li>
            <li>In a large bowl, mix flour, sugar, baking powder, and salt.</li>
            <li>In another bowl, whisk melted butter, eggs, milk, and vanilla extract.</li>
            <li>Combine wet and dry ingredients, then gently fold in the berries.</li>
            <li>Spoon the batter into the muffin tin, filling each liner about 3/4 full.</li>
            <li>Bake for 20–25 minutes or until a toothpick inserted in the center comes out clean.</li>
            <li>Cool the muffins completely, then dust with powdered sugar and garnish with extra berries if desired.</li>
        </ol>
    </div>
